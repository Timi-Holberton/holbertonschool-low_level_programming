#include <stdio.h>
#include "main.h"

/*
 * add - Additionne deux nombres
 * @nbre1: Le premier nombre à additionner
 * @nbre2: Le deuxième nombre à additionner
 *
 * Description: Cette fonction prend deux entiers en paramètres et retourne
 * leur somme. Il n'y a pas de gestion de cas particulier, car la fonction
 * additionne simplement les deux nombres.
 *
 * Return: La somme des deux nombres passés en paramètres.
 */
int add(int nbre1, int nbre2)
{
    /* Retourne la somme des deux nombres */
    return (nbre1 + nbre2);  /* Additionne les deux nombres et renvoie le résultat */
}
